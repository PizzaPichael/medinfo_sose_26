import { v4 as uuidv4 } from 'uuid'

/**
 * Consent Retrieval Service
 * Handles business logic for consent operations:
 * - Transforming request data into schema format
 * - Validating consent data
 * - Generating unique consent IDs
 * - Preparing consent objects for database operations
 */
class ConsentRetrieval {
    /**
     * Constructor
     * @param {DataBaseClient} dbClient - Database client for consent operations
     */
    constructor(dbClient) {
        this.dbClient = dbClient
        console.log('[CONSENT SERVICE] Created...')
    }

    /**
     * Generates a unique consent ID using UUID
     * @returns {string} Unique consent ID
     */
    generateConsentId = () => {
        return `consent-${uuidv4()}`
    }

    /**
     * Creates a consent schema instance from request body
     * Validates and transforms request data into the format expected by the database
     * Generates a unique consentId
     * 
     * @param {Object} consentData - Request body containing consent information
     * @param {string} consentData.status - Status of the consent, e.g. "active" (required)
     * @param {string} consentData.decision - "permit" or "deny" (required)
     * @param {Array} consentData.provision - Array of provision objects with period info (required)
     * @param {Object} consentData.subject - Patient reference object (required)
     * @param {string} consentData.subject.reference - Patient ID, e.g. "Patient/123" (required)
     * @param {string} consentData.subject.display - Optional patient name
     * @param {Array} consentData.category - Optional category information
     * @param {Array} consentData.controller - Optional controller information
     * @param {Array} consentData.sourceAttachment - Optional source attachment
     * @param {Array} consentData.regulatoryBasis - Optional regulatory basis
     * 
     * @returns {Object} Consent schema instance ready for database
     * @throws {Error} If required fields are missing or invalid
     */
    buildConsentSchema = (consentData) => {
        try {
            // Validate required fields
            this.validateConsentData(consentData)

            // Generate consentId
            const consentId = this.generateConsentId()

            // Transform request data into schema format
            const consentSchemaData = {
                consentId: consentId,
                status: consentData.status,
                decision: consentData.decision,
                provision: consentData.provision || [],
                
                // Optional fields
                ...(consentData.id && { id: consentData.id }),
                ...(consentData.text && { text: consentData.text }),
                ...(consentData.subject && { subject: consentData.subject }),
                ...(consentData.date && { date: consentData.date }),
                ...(consentData.category && { category: consentData.category }),
                ...(consentData.controller && { controller: consentData.controller }),
                ...(consentData.sourceAttachment && { sourceAttachment: consentData.sourceAttachment }),
                ...(consentData.regulatoryBasis && { regulatoryBasis: consentData.regulatoryBasis })
            }

            console.log('[CONSENT SERVICE] Consent schema built successfully')
            return consentSchemaData
        } catch (error) {
            console.error('[CONSENT SERVICE] Error building consent schema:', error.message)
            throw error
        }
    }

    /**
     * Validates that required consent fields are present and valid
     * Note: consentId is NOT required as it will be auto-generated by the service
     * 
     * @param {Object} consentData - Consent data to validate
     * @throws {Error} If validation fails
     */
    validateConsentData = (consentData) => {
        const requiredFields = ['status', 'decision', 'provision', 'subject']
        
        for (const field of requiredFields) {
            if (!consentData[field]) {
                throw new Error(`Missing required field: ${field}`)
            }
        }

        // Validate subject has reference (patient ID)
        if (!consentData.subject || !consentData.subject.reference) {
            throw new Error('subject.reference (patient ID) is required')
        }

        // Validate decision is either "permit" or "deny"
        if (!['permit', 'deny'].includes(consentData.decision)) {
            throw new Error('decision must be either "permit" or "deny"')
        }

        // Validate provision is an array
        if (!Array.isArray(consentData.provision)) {
            throw new Error('provision must be an array')
        }

        console.log('[CONSENT SERVICE] Consent data validated successfully')
    }

    /**
     * Checks if a consent is valid
     * Valid consent must have:
     * - status: "active"
     * - decision: "permit"
     * - provision period that includes today's date
     * 
     * @param {Object} consent - Consent document from database
     * @returns {Object} { isValid: boolean, reason?: string }
     */
    isConsentValid = (consent) => {
        try {
            if (!consent) {
                return { isValid: false, reason: 'No consent found' }
            }

            // Check status is active
            if (consent.status !== 'active') {
                return { isValid: false, reason: `Consent status is "${consent.status}", not "active"` }
            }

            // Check decision is permit
            if (consent.decision !== 'permit') {
                return { isValid: false, reason: `Consent decision is "${consent.decision}", not "permit"` }
            }

            // Check provision period validity
            if (consent.provision && Array.isArray(consent.provision) && consent.provision.length > 0) {
                const now = new Date()
                let isWithinValidPeriod = false

                for (const provision of consent.provision) {
                    if (provision.period) {
                        const startDate = provision.period.start ? new Date(provision.period.start) : null
                        const endDate = provision.period.end ? new Date(provision.period.end) : null

                        // Check if current date is within the provision period
                        const afterStart = !startDate || now >= startDate
                        const beforeEnd = !endDate || now <= endDate

                        if (afterStart && beforeEnd) {
                            isWithinValidPeriod = true
                            break
                        }
                    }
                }

                if (!isWithinValidPeriod) {
                    return { isValid: false, reason: 'Current date is outside the provision period' }
                }
            }

            console.log('[CONSENT SERVICE] Consent validation passed')
            return { isValid: true }
        } catch (error) {
            console.error('[CONSENT SERVICE] Error validating consent:', error.message)
            throw error
        }
    }

    /**
     * Retrieves and validates consent for a patient
     * Service responsibility: Business logic for checking consent validity
     * 
     * @param {string} patientId - Patient ID to check consent for
     * @returns {Object} { consent: Object, isValid: boolean, reason?: string }
     * @throws {Error} If retrieval or validation fails
     */
    checkConsentForPatient = async (patientId) => {
        try {
            // DB Client retrieves the consent
            const consent = await this.dbClient.getConsentByPatientId(patientId)

            // Service validates the consent
            const validationResult = this.isConsentValid(consent)

            return {
                consent: consent,
                ...validationResult
            }
        } catch (error) {
            console.error('[CONSENT SERVICE] Error checking consent for patient:', error.message)
            throw error
        }
    }
}

export default ConsentRetrieval